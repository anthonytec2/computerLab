    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       ArrayList<String> kol=u.find(teacherName);//finds all occurences of teacher name
        if(jTextField1.getText().length()==0){//checks if the number you want to delete is entered
            
        }else{
            int d=0;//sets up the number you want to delete
            try{ 
               d=Integer.parseInt(jTextField1.getText());//checks if the number you want to delete is a number and parses it to a number
               
            } 
            catch (NumberFormatException e){ //if the data entered is not a number then a NAN message is displayed
              JOptionPane.showMessageDialog(null, "ERROR Number was not entered");  
             return;   
             }                  
           if(kol.size()-1<d){//Checks if the number you entered is greater then the number of bookings found
               JOptionPane.showMessageDialog(null, "ERROR Not that many list items");  
           }else{
          
          u.delete(teacherName,d);//deleted the booking found
          
           }
        }
        this.formWindowOpened(null);//refreshes found bookings screen
    }//GEN-LAST:event_jButton1ActionPerformed

