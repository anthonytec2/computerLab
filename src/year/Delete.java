    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
     ArrayList<String> kol=u.find(teacherName);//find the occurences of the teachername in the bookings array
     DefaultListModel model1 =new DefaultListModel(); //a new model is created in order to display all found occurences
     

     for(int i=0;kol.size()>i;i=i+1){//Goes through the arraylist
            int x=Integer.parseInt(kol.get(i).substring(2));//gets the day from the array lists element
            Calendar caq=Calendar.getInstance();//makes a calendar object with current date
	    caq.set(Calendar.DAY_OF_YEAR, x);//sets a calendar with the day of the year
            String e=(caq.get(Calendar.MONTH)+1)+"/"+caq.get(Calendar.DATE)+"/"+caq.get(Calendar.YEAR);//gets a m d y from the 0-364 day of the year value
           model1.addElement(String.valueOf(i)+u.returnCl(Integer.parseInt(kol.get(i).substring(0,1)))+" Period"+(Integer.parseInt(kol.get(i).substring(1,2))+1)+" Day"+e);//displays cl # date and period of booking
    }
     jList1.setModel(model1);//displays list
     
    }//GEN-LAST:event_formWindowOpened
